// Boolean and Comparison Operators


// - Booleans: True/False
// - Comparison Operators: <,>,<=,>=,=

let num1 = 5;
let num2 = 7;

console.log(num1>num2);
console.log(num1<num2);




// == vs ===


num3 = "7";
num4 = 7;

console.log(num3 == num4);                 // == checks if the values are same or not
console.log(num3 === num4);                // === checks if both DataTypes as well as values are same or not




// != vs !==


num5 = "7";
num6 = 7;

console.log(num5 != num6);                 // != checks if the value are same or not
console.log(num5 !== num6);                // !== checks if both the DataType as well as values are same or not

