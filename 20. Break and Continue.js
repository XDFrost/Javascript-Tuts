// Break and Continue keyword




// Break
// - Terminates the execution of the block int which break is used

for(let i = 0; i<=10; i++) {
    if(i===4) {
        break;
    }
    console.log(i);
}



// Continue
// - Skips an iteration in the block

for(let i = 0; i<=10; i++) {
    if(i===4) {
        continue;
    }
    console.log(i);
}
